#---- Repeticoes - while enquanto    for - para controlado
# %%

numero = int(input("Qual numero da tabuada"))


for x in range(10,0,-1):
    print (x, " * ", numero, " = ", x * numero)








#%%
while x <= 10:
    print (x, " * ", numero, " = ", x * numero)
    x = x + 1



